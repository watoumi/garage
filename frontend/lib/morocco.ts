function normCity(s: string): string {
  return s.toLowerCase().normalize("NFD").replace(/[\u0300-\u036f]/g, "").trim();
}

export function cityCoords(city?: string | null): [number, number] | null {
  if (!city) return null;
  return CITY_COORDS[normCity(city)] ?? null;
}

